@operator SÀ£ function S_x(elt::Type{<:Number}, ::Type{Trivial}; spin = 1 // 2)
    S_x_mat, = spinmatrices(spin, elt)
    pspace = spin_space(Trivial; spin)
    return TensorMap(S_x_mat, pspace ‚Üê pspace)
end

