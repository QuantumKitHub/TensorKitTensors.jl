"""
    spin_space([symmetry::Type{<:Sector}]; spin=1 // 2)

Return the local Hilbert space for a spin-`spin` site with the given symmetry.

| Symmetry | Space |
|---|---|
| `Trivial` | `ComplexSpace(2s+1)` |
| `Z2Irrep` | `Z2Space(0 => 1, 1 => 1)` (spin-1/2 only) |
| `U1Irrep` | `U1Space(m => 1 for m in -s:s)` |
| `SU2Irrep` | `SU2Space(s => 1)` |
"""
spin_space(::Type{Trivial} = Trivial; spin = 1 // 2) = ComplexSpace(Int(2 * spin + 1))
function spin_space(::Type{Z2Irrep}; spin = 1 // 2)
    spin == 1 // 2 || throw(ArgumentError("Z2 symmetry only implemented for spin 1//2"))
    return Z2Space(0 => 1, 1 => 1)
end
spin_space(::Type{U1Irrep}; spin = 1 // 2) = U1Space(i => 1 for i in (-spin):spin)
spin_space(::Type{SU2Irrep}; spin = 1 // 2) = SU2Space(spin => 1)
spin_space(symmetry::Type{<:Sector}; kwargs...) = throw(ArgumentError("invalid symmetry `$symmetry`"))

"""
    basis_transform(symmetry::Type{<:Sector}; spin = 1 // 2)

Return the unitary basis transformation that maps the standard spin basis ``\\{|s‚ü©, |s-1‚ü©, ‚Ä¶, |-s‚ü©\\}``
(the eigenbasis of ``S^z`` in descending order of ``m``, as used by the `Trivial` operators)
onto the basis of `spin_space(symmetry; spin)`, as a `TensorMap` on `spin_space(Trivial; spin)` to `desymmetrize(spin_space(symmetry; spin))`.
See also [`symmetrize`](@ref TensorKitTensors.symmetrize).

| Symmetry | Transformation |
|---|---|
| `Trivial` | identity |
| `Z2Irrep` | Hadamard matrix ``\\begin{pmatrix} 1 & 1 \\\\ 1 & -1 \\end{pmatrix}/\\sqrt{2}`` |
| `U1Irrep` | permutation to the order of `sectors(spin_space(U1Irrep; spin))` |
| `SU2Irrep` | identity |

For `Z2Irrep` (spin-1/2 only), the symmetry is the ``‚Ñ§‚ÇÇ`` spin-flip generated by ``2S^x``,
and the Hadamard matrix maps onto its eigenbasis ``|¬±‚ü© = (|‚Üë‚ü© ¬± |‚Üì‚ü©)/‚àö2``, where
`Z2Irrep(0)` labels the flip-symmetric state ``|+‚ü©``.

For `U1Irrep`, the ``S^z`` eigenvalue ``m`` is used as the ``U(1)`` charge, and the
transformation is the permutation from descending-``m`` order to TensorKit's charge order
(``0, 1, -1, 2, ‚Ä¶`` for integer spin, ``1/2, -1/2, 3/2, ‚Ä¶`` for half-integer spin).

For `SU2Irrep`, TensorKit's basis convention within an irrep is descending ``m``, which
coincides with the standard spin basis, such that the transformation is the identity.
"""
function basis_transform(::Type{Trivial}; spin = 1 // 2)
    V = spin_space(Trivial; spin)
    d = Int(2 * spin + 1)
    return TensorMap(Matrix{Int}(I, d, d), V ‚Üê V)
end
function basis_transform(::Type{Z2Irrep}; spin = 1 // 2)
    spin == 1 // 2 || throw(ArgumentError("Z2 symmetry only implemented for spin 1//2"))
    h = signedroot(1 // 2)
    V = spin_space(Trivial; spin)
    return TensorMap([h h; h -h], desymmetrize(spin_space(Z2Irrep; spin)) ‚Üê V)
end
function basis_transform(::Type{U1Irrep}; spin = 1 // 2)
    V = spin_space(U1Irrep; spin)
    d = Int(2 * spin + 1)
    U = zeros(Int, d, d)
    for (row, c) in enumerate(sectors(V))
        U[row, Int(spin - c.charge + 1)] = 1
    end
    return TensorMap(U, desymmetrize(V) ‚Üê spin_space(Trivial; spin))
end
function basis_transform(::Type{SU2Irrep}; spin = 1 // 2)
    V = spin_space(Trivial; spin)
    d = Int(2 * spin + 1)
    return TensorMap(Matrix{Int}(I, d, d), desymmetrize(spin_space(SU2Irrep; spin)) ‚Üê V)
end

# Symmetrize a spin operator through its basis transformation
_symmetrize_operator(O::AbstractTensorMap, symmetry::Type{<:Sector}; kwargs...) =
    symmetrize(O, basis_transform(symmetry; kwargs...), spin_space(symmetry; kwargs...))

# Pauli matrices
# --------------
function _pauliterm(spin, i, j)
    1 <= i <= 2 * spin + 1 || return 0.0
    1 <= j <= 2 * spin + 1 || return 0.0
    return sqrt((spin + 1) * (i + j - 1) - i * j) / 2.0
end

"""
    casimir(spin::Real)
    casimir(c::SU2Irrep)

Return the value of the quadratic Casimir ``C_2(s) = s(s+1)`` for SU(2).
"""
casimir(spin::Real) = spin * (spin + 1)
casimir(c::SU2Irrep) = casimir(c.j)

"""
    spinmatrices(spin [, eltype])

The spinmatrices according to [Wikipedia](https://en.wikipedia.org/wiki/Spin_(physics)#Higher_spins).
"""
function spinmatrices(spin, elt = ComplexF64)
    N = Int(2 * spin)

    Sx = zeros(elt, N + 1, N + 1)
    Sy = zeros(complex(elt), N + 1, N + 1)
    Sz = zeros(elt, N + 1, N + 1)

    for row in 1:(N + 1)
        for col in 1:(N + 1)
            term = _pauliterm(spin, row, col)

            if (row + 1 == col)
                Sx[row, col] += term
                Sy[row, col] -= 1im * term
            end

            if (row == col + 1)
                Sx[row, col] += term
                Sy[row, col] += 1im * term
            end

            if (row == col)
                Sz[row, col] += spin + 1 - row
            end
        end
    end
    return Sx, Sy, Sz, one(Sx)
end

